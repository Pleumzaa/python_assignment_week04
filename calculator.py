def add(num1, num2):
    sum = num1 + num2
    return sum 
    
def subtract(num1, num2):
    sum = num1 - num2
    return sum 

def multiply(num1, num2):
    sum = num1*num2
    return sum

def divide(num1, num2):
    sum = num1/num2
    return sum

num1 = 10
num2 = 5
print(add(num1,num2))
print(subtract(num1,num2))
print(multiply(num1,num2))
print(divide(num1,num2))